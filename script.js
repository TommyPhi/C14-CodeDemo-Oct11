// for loop example function
let forLoop = () => {
    let array=['a', 'b', 'c', 'd']
    for (let i=0; i<array.length; i++) {
        console.log(array[i])
    }
}

// while loop example function
let whileLoop = () => {
    let x = 0;
    while (x<=10) {
        console.log(`${x} chickens crossed the road`);
        x++;
    }
}

// do...while loop example function
let doWhileLoop = () => {
    let y = 0;
    do {
        y++;
        console.log(`You leveled up! You are now level: ${y}`)
    }
    while (y < 10);
}

